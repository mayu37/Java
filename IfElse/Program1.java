//Write a java program to check if a number is even or odd.
package IfElse;

class Program1 {
	
	public static void main(String[] args) {
		 int x = 10;
		 if(x%2==0) {
			 System.out.println(x +" is an Even Number..");
		 }else {
			 System.out.println(x +" is an ODD Number..");
		 }
	}
	

}
