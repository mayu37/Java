package NestedForLoop2;

public class Program11 {
	public static void main(String[] args) {
		int num = 4;
		for(int i=1; i<=num;i++) {
			for(int j=1; j<=i;j++) {
				System.out.print("C2W ");
			}
			System.out.println();
		}
	}
	

}
