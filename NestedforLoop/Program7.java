package NestedforLoop;

public class Program7 {
	public static void main(String[] args) {
		char ch = 'D';
		for(int i = 1;i<=5;i++) {
			for(int j = 1;j<=5;j++) {
				System.out.print(ch+ " ");
			}
			System.out.println();
		}
	}

}
